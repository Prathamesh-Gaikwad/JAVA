class MultipleTryCatch
{
    public static void main(String args[])
    {
        int result;

        try
        {
            System.out.println("\n\tWe are now generating Exception1 : ");
            System.out.println("\n\t args[0] : " + args[0]);

            System.out.println("\n\tWe are now generating Exception2 : ");
            result = Integer.parseInt(args[0]);

            System.out.println("\n\tWe are now generating Exception3 : ");
            result = 10 / 0;
        }
        catch(NumberFormatException exceptionObj)
        {
            System.out.println("\n\tYes !! Exception Caught Successfull !!");
            System.out.println("\n\t Exception : " + exceptionObj);
        }
        catch(ArithmeticException exceptionObj)
        {
            System.out.println("\n\tYes !! Exception Caught Successfull !!");
            System.out.println("\n\t Exception : " + exceptionObj);
        }
        catch(Exception exceptionObj)
        {
            System.out.println("\n\tYes !! Exception Caught Successfull !!");
            System.out.println("\n\t Exception : " + exceptionObj);
        }

        System.out.println("\n\tLeaving main()");
    }
}
